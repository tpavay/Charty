/// Enum comprised of all the primary chart types offered by the Swift Charts framework
enum ChartType: CaseIterable {
    case barChart
    case lineChart
    case areaChart
    case pointChart
    case ruleChart
    case rectangleChart
    case symbolChart
    case pieChart
    case donutChart
    case heatmapChart
}
